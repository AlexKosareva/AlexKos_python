my_list = list(range(18, 1, -4))
print(my_list)


# Функция range(start, stop, step) генерирует последовательность чисел:
# start (Начальное значение): 18
# stop (Конечное значение, не включая его): 1 (мы останавливаемся до достижения 1, то есть включаем 2)
# step (Шаг/Интервал): -4 (убываем на 4 каждую итерацию)